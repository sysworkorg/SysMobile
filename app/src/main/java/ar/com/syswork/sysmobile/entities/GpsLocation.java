package ar.com.syswork.sysmobile.entities;

public class GpsLocation {
	
	private int gpsX;
	private int gpsY;

	public int getGpsX() 
	{
		return gpsX;
	}
	
	public void setGpsX(int gpsX) 
	{
		this.gpsX = gpsX;
	}
	
	public int getGpsY() 
	{
		return gpsY;
	}
	public void setGpsY(int gpsY) 
	{
		this.gpsY = gpsY;
	}
	
}
